package io.metadew.iesi.data.definition;


public class DataField {

    private String name;
    private String value;

    //Constructors
    public DataField() {

    }

    //Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

}